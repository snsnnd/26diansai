static uint8_t dmp_read_byte(dt_mpu6050_dmp_t *dmp, uint8_t reg)
{
    return soft_iic_read_8bit_register(&dmp->iic, reg);
}

static uint16_t dmp_read_16(dt_mpu6050_dmp_t *dmp, uint8_t reg)
{
    uint8_t h = dmp_read_byte(dmp, reg);
    uint8_t l = dmp_read_byte(dmp, reg + 1);
    return (uint16_t)((h << 8) | l);
}

uint8_t dt_mpu6050_dmp_read(dt_mpu6050_dmp_t *dmp, dt_dmp_data_t *out)
{
    uint8_t buf[28];
    int16_t q[4];
    int16_t g[3];
    uint16_t fifo_count;
    uint16_t packets;

    fifo_count = dmp_read_16(dmp, REG_FIFO_COUNT_H);
    if (fifo_count < 28) return 0;

    packets = fifo_count / 28;
    if (packets > 2) packets = 2;

    while (packets--)
    {
        soft_iic_read_8bit_registers(&dmp->iic, REG_FIFO_R_W, buf, 28);

        q[0] = (int16_t)((buf[0]  << 8) | buf[1]);
        q[1] = (int16_t)((buf[4]  << 8) | buf[5]);
        q[2] = (int16_t)((buf[8]  << 8) | buf[9]);
        q[3] = (int16_t)((buf[12] << 8) | buf[13]);
        g[0] = (int16_t)((buf[16] << 8) | buf[17]);
        g[1] = (int16_t)((buf[20] << 8) | buf[21]);
        g[2] = (int16_t)((buf[24] << 8) | buf[25]);
    }

    float q0 = (float)q[0] / 16384.0f;
    float q1 = (float)q[1] / 16384.0f;
    float q2 = (float)q[2] / 16384.0f;
    float q3 = (float)q[3] / 16384.0f;

    out->roll  = atan2f(q0 * q1 + q2 * q3, 0.5f - q1 * q1 - q2 * q2) * 57.29578f;
    out->pitch = asinf(-2.0f * (q1 * q3 - q0 * q2)) * 57.29578f;
    out->yaw   = atan2f(q1 * q2 + q0 * q3, 0.5f - q2 * q2 - q3 * q3) * 57.29578f;

    return 1;
}
