    dmp_write_mem(dmp, 0x01B2, dmp_updates, sizeof(dmp_updates));

    soft_iic_write_8bit_register(&dmp->iic, REG_INT_ENABLE, 0x00);
    soft_iic_write_8bit_register(&dmp->iic, REG_FIFO_EN, 0x00);
    soft_iic_write_8bit_register(&dmp->iic, REG_USER_CTRL, 0x0c);
