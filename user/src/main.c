/*********************************************************************************************************************
 * main.c — MSPM0G3507 智能车主程序入口
 *
 * 本文件是整个智能车系统的入口点和主循环调度器。
 * 启动流程：
 *   1. clock_init — 配置系统时钟为 80MHz（MSPM0G3507 最高主频）
 *   2. debug_init — 初始化逐飞库的调试串口（printf 输出）
 *   3. system_delay_ms(500) — 等待片上外设和外部模块上电稳定
 *   4. ec_app_init — 应用层初始化（电机、传感器、控制环等）
 *   5. ec_app_run — 应用主循环（周期调度各控制任务）
 *
 * 设计哲学：
 *   主循环极其精简——所有业务逻辑由 ec_app 模块管理。
 *   这种"init + super-loop"模式在嵌入式实时控制中非常常见，
 *   适合智能车这种不需要复杂 RTOS 的硬实时控制场景。
 ********************************************************************************************************************/

#include "zf_common_headfile.h"   /* 逐飞科技 MSPM0 开源库通用头文件 */
#include "framework/ec_app.h"      /* 应用层框架（上位调度器） */
#include "driver/dt_rgb.h"
/*
 * main — 系统入口函数。
 *
 * 单片机复位后执行的第一段代码，负责初始化系统和进入主循环。
 * 注意：在 MSPM0G3507 上，startup_ccs.c 或 startup_iar.c 会在
 * 调用 main 之前完成 .bss 清零、.data 初始化等 C 运行时设置。
 *
 * @return: 嵌入式 main 通常不应返回，此处留返回值仅为符合 C 标准
 */
int main(void)
{
    /*
     * 第一阶段：系统初始化
     * 将系统时钟配置为 80 MHz（PLL 倍频自 32MHz 内部 OSC 或外部晶振）。
     * debug_init 初始化 printf 重定向到的调试串口。
     */
    clock_init(SYSTEM_CLOCK_80M);
    debug_init();

    /*
     * 等待 500ms 让外部模块上电稳定。
     * 特别是 MPU6050（陀螺仪）、T8 灰度传感器等需要上电自检时间的模块。
     */
    system_delay_ms(500);

    /*
     * 第二阶段：应用层初始化
     * ec_app_init 负责初始化所有外设、控制回路、任务调度器。
     * 具体初始化哪些内容由 EC_APP_PROFILE 配置宏决定
     * （硬件测试 / 循迹小车 / 空工程）。
     */
    ec_app_init();

    /*
     * 第三阶段：主循环（超级循环 / Super Loop）
     * 永不退出，持续调用 ec_app_run。
     * ec_app_run 内部实现了一个时间片轮转调度器，
     * 以固定周期执行各个控制任务（循线、PID 更新、日志输出等）。
     */
    while (1) { ec_app_run(); }

}
